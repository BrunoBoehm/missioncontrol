class Users::OmniauthCallbacksController < Devise::OmniauthCallbacksController

  def facebook
    @user = User.from_omniauth(request.env["omniauth.auth"])
    # information retrieved from Facebook by OmniAuth is available as a hash at request.env["omniauth.auth"]
    # the .from_omniauth method is defined in our User models

    if @user.persisted?
      # when a valid user is found, they are signed in by Devise's sign_in_and_redirect method
      # sign_in_and_redirect @user
      sign_in(@user)
      redirect_to new_account_path
      set_flash_message(:notice, :success, :kind => "Facebook") if is_navigational_format?
    else
      session["devise.facebook_data"] = request.env["omniauth.auth"]
      redirect_to new_user_registration_url
    end    
  end

  def failure
    redirect_to root_path
  end

